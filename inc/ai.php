function mini_ai_settings_init() {
    register_setting( 'mini_ai', 'mini_ai_settings', [
        'sanitize_callback' => 'mini_ai_sanitize_settings',
    ] );
    add_settings_section(
        'mini_ai_section',
        __( 'AI settings', 'mini' ),
        '__return_false',
        'mini-ai'
    );
}
add_action( 'admin_init', 'mini_ai_settings_init' );

function mini_ai_sanitize_settings( $input ) {
    $input = is_array( $input ) ? $input : [];

    $input['mini_ai_enabled']       = ! empty( $input['mini_ai_enabled'] ) ? 1 : 0;
    $input['mini_ai_global_policy'] = in_array( $input['mini_ai_global_policy'] ?? '', [ 'allow', 'disallow', 'custom' ], true )
        ? $input['mini_ai_global_policy']
        : 'allow';

    if ( isset( $input['mini_ai_custom_rules'] ) ) {
        $input['mini_ai_custom_rules'] = sanitize_textarea_field( $input['mini_ai_custom_rules'] );
    }

    $known_bots  = mini_ai_get_known_bots();
    $bot_policies = [];
    foreach ( $known_bots as $bot_key => $bot_info ) {
        $policy = $input['mini_ai_bot_policies'][ $bot_key ] ?? 'inherit';
        $bot_policies[ $bot_key ] = in_array( $policy, [ 'inherit', 'allow', 'disallow' ], true ) ? $policy : 'inherit';
    }
    $input['mini_ai_bot_policies'] = $bot_policies;

    return $input;
}

