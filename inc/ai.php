function mini_ai_get_txt_content() {
    $opts          = get_option( 'mini_ai_settings', [] );
    $global_policy = $opts['mini_ai_global_policy'] ?? 'allow';
    $bot_policies  = $opts['mini_ai_bot_policies']  ?? [];
    $known_bots    = mini_ai_get_known_bots();

    if ( $global_policy === 'custom' ) {
        $custom = trim( $opts['mini_ai_custom_rules'] ?? '' );
        if ( $custom !== '' ) {
            return $custom . "\n";
        }
        // Empty custom field — fall through to default generated content.
    }

    $lines = [];
    $lines[] = '# ai.txt — AI crawler policy';
    $lines[] = '# Generated by mini plugin — ' . home_url( '/ai.txt' );
    $lines[] = '';
    $lines[] = 'User-agent: *';
    $lines[] = ( $global_policy === 'allow' ) ? 'Allow: /' : 'Disallow: /';

    // Per-bot overrides
    foreach ( $known_bots as $bot_key => $bot_info ) {
        $policy = $bot_policies[ $bot_key ] ?? 'inherit';
        if ( $policy === 'inherit' ) {
            continue;
        }
        $lines[] = '';
        $lines[] = '# ' . $bot_info['company'];
        $lines[] = 'User-agent: ' . $bot_info['name'];
        $lines[] = ( $policy === 'allow' ) ? 'Allow: /' : 'Disallow: /';
    }

    $lines[] = '';

    return implode( "\n", $lines );
}

