/* START - ai.txt endpoint */

function mini_ai_rewrite_version() {
    return '1';
}

function mini_ai_register_rewrite_rules() {
    add_rewrite_rule( '^ai\.txt$', 'index.php?mini_ai_txt=1', 'top' );
}
add_action( 'init', 'mini_ai_register_rewrite_rules' );

function mini_ai_query_vars( $vars ) {
    $vars[] = 'mini_ai_txt';
    return $vars;
}
add_filter( 'query_vars', 'mini_ai_query_vars' );

function mini_ai_serve_txt() {
    if ( ! get_query_var( 'mini_ai_txt' ) ) {
        return;
    }

    $opts = get_option( 'mini_ai_settings', [] );

    if ( empty( $opts['mini_ai_enabled'] ) ) {
        status_header( 404 );
        exit;
    }

    $content = mini_ai_get_txt_content();

    header( 'Content-Type: text/plain; charset=UTF-8' );
    header( 'Cache-Control: public, max-age=3600' );
    echo $content;
    exit;
}
add_action( 'template_redirect', 'mini_ai_serve_txt' );

function mini_ai_maybe_flush_rewrite_rules() {
    if ( ! is_admin() || ! current_user_can( 'manage_options' ) ) {
        return;
    }

    $current = get_option( 'mini_ai_rules_version' );
    $target  = mini_ai_rewrite_version();

    if ( $current === $target ) {
        return;
    }

    mini_ai_register_rewrite_rules();
    flush_rewrite_rules( false );
    update_option( 'mini_ai_rules_version', $target, false );
}
add_action( 'admin_init', 'mini_ai_maybe_flush_rewrite_rules' );

function mini_ai_activation_setup() {
    mini_ai_register_rewrite_rules();
    flush_rewrite_rules();
    update_option( 'mini_ai_rules_version', mini_ai_rewrite_version(), false );
}

function mini_ai_deactivation_teardown() {
    flush_rewrite_rules();
}

